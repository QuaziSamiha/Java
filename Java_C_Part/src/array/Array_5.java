package array;

// array length : length is variable which hold size of array.

public class Array_5 {

    public static void main(String[] args) {

        int[] array_1 = new int[6];
        System.out.println("array_1.length : " +array_1.length);

        int array_2[] = new int[10];
        int variable = array_2.length;
        System.out.println("array_2.length : " +variable);

        int[] array_3 = new int[4];
        System.out.println(array_3.length);
    }
}
