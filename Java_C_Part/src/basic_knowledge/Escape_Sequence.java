package basic_knowledge;

public class Escape_Sequence { // public keywords means this class can be used by any class

    public static void main(String[] variable_name) {

        System.out.println("Java\tProgram_tab"); // for tab

        System.out.println("Java\nProgram_newline"); // new line

        System.out.println(" \"Java Program\" "); // double quote

        System.out.println(" \'Java Program\' "); // single quote

        System.out.println("Java\rProgram_carriage_return"); // carriage return

        System.out.println("Java\bProgram_backspace");
    }
}

