package this_keyword;

public class This_Keyword_1 {

    // instance variables
    String v1;
    int v2;

    This_Keyword_1(String v1, int v2){ // constructor
        this.v1 = v1;
        this.v2 = v2;
    }

    void instance_method(){
        System.out.println("v1 = " +v1);
        System.out.println("v2 = " +v2);
    }
}

